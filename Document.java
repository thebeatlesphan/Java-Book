public class Document
{
    private String text;

    public Document()
    {
        text = "what";
    }

    public Document(String newText)
    {
        text = newText;
    }

    public String toString()
    {
        return text;
    }

    public String getText()
    {
        return text;
    }

    public void setString(String newText)
    {
        text = newText;
    }
}
